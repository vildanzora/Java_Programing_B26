package day11_if_statements;

public class SingleIfExamples {
    public static void main(String[] args) {

        if(false){ // value is false. Hence, it wouldn't write anything.

            System.out.println("Hello World"); // we will not see in output because false

        }


        if(true){
            System.out.println("Today is Monday");

        }
        System.out.println("Hello World"); // we will see in output because the statement out of the if bracket parenthesis {}




    }



}
