package day08_relational_operators;

public class OperatorPractice2 {

    public static void main(String[] args) {
        int a = 40;
        System.out.println(--a); //print 39
        System.out.println(--a); //print 38
        System.out.println(a--); // print 38 after 37
        System.out.println(--a); // print 36
        System.out.println(--a); // print 35
        System.out.println(a--); // print 35 after 34
        System.out.println(a); // print 34
    }
}
