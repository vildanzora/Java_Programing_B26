package day08_relational_operators;

public class RelationalOperatorsExamples {

    public static void main(String[] args) {
        System.out.println(10>5);
        System.out.println(10<5);

        int a =5;
        int b= 3;
        System.out.println(a>5);
        System.out.println(b<a);

        boolean aLessThan = a<b;
        System.out.println(aLessThan);

        System.out.println(6>=6); // Relational operators results in boolean
        System.out.println(6<=6);

        System.out.println(4==4); // equal equal means just normal equal

        System.out.println(4!=4);
        System.out.println(3 != 1); // true 3 not equal 1



    }
}
