package day26_nested_loop;

public class NestedLoopExample {
    public static void main(String[] args) {

        for (int i = 0; i < 5 ;i++){
            System.out.println("Hello World");
        }
        System.out.println("Hello universe\n");

        for (int i = 0; i < 5 ;i++){
            System.out.println("Hello World");
        }
        System.out.println("Hello universe\n");

        for (int i = 0; i < 5 ;i++){
            System.out.println("Hello World");
        }
        System.out.println("Hello universe\n");

        System.out.println("------------------------");

        // outer loop
        for ( int j = 0; j <3; j++  ){

            // inner loop
            for (int i = 0; i < 5 ;i++){
                System.out.println("Hello World");
            }
            System.out.println("Hello universe\n");

        } // main loop end










    }
}
